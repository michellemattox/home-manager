import { useQuery, useMutation, useQueryClient } from "@tanstack/react-query";
import { supabase } from "@/lib/supabase";
import { useHouseholdStore } from "@/stores/householdStore";
import type { Household, HouseholdMember, HouseholdInvite } from "@/types/app.types";

export function useHousehold(householdId: string | undefined) {
  return useQuery({
    queryKey: ["household", householdId],
    queryFn: async () => {
      if (!householdId) return null;
      const { data, error } = await supabase
        .from("households")
        .select("*")
        .eq("id", householdId)
        .single();
      if (error) throw error;
      return data as Household;
    },
    enabled: !!householdId,
  });
}

export function useHouseholdMembers(householdId: string | undefined) {
  return useQuery({
    queryKey: ["household_members", householdId],
    queryFn: async () => {
      if (!householdId) return [];
      const { data, error } = await supabase
        .from("household_members")
        .select("*")
        .eq("household_id", householdId)
        .order("joined_at");
      if (error) throw error;
      return (data ?? []) as HouseholdMember[];
    },
    enabled: !!householdId,
  });
}

export function useCreateHousehold() {
  const qc = useQueryClient();
  return useMutation({
    mutationFn: async ({
      name,
      zipCode,
      userId,
      displayName,
    }: {
      name: string;
      zipCode: string;
      userId: string;
      displayName: string;
    }) => {
      const { data, error } = await supabase.rpc("create_household_with_member", {
        p_name: name,
        p_zip_code: zipCode,
        p_user_id: userId,
        p_display_name: displayName,
      });
      if (error) throw error;
      return data as Household;
    },
    onSuccess: () => qc.invalidateQueries({ queryKey: ["household"] }),
  });
}

